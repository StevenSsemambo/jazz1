// Main story function — picks right category and presents with Jazz personality
function tellStory(mood, ageGroup, storyType) {
  let category = 'motivational';

  if (storyType) {
    category = storyType;
  } else if (ageGroup === 'child') {
    category = mood === 'excited' ? 'kids_adventure' : 'kids_bedtime';
  } else {
    // Pick based on mood and intent
    const moodMap = {
      sad: 'motivational', anxious: 'adult_bedtime', tired: 'adult_bedtime',
      happy: 'funny', excited: 'funny', grateful: 'love',
      lonely: 'love', angry: 'motivational', neutral: 'historical',
      bored: 'funny', curious: 'wildFacts',
    };
    category = moodMap[mood] || (['african','historical','wildFacts','funny','love'][Math.floor(Math.random()*5)]);
  }

  const pool = STORIES[category];
  if (!pool || !pool.length) return null;

  const story = pool[Math.floor(Math.random() * pool.length)];
  const intro = rnd(STORY_INTROS[category] || ['Here is a story for you.']);
  const outro = rnd(STORY_OUTROS[category] || ['What did you think?']);

  if (typeof story === 'string') {
    // It's a wild fact
    return { intro: '', text: story, outro, title: '' };
  }

  return { intro, text: story.text, outro, title: story.title };
}

